    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        // TODO add your handling code here:
        switch (currentQuestion) {
            case 2:
            loadQuestion("Which planet is known as the 'Red planet'?"
                , "Venus", "Mars", "Jupiter", "Saturn");
            break;
            case 3:
            loadQuestion("What is the powerhouse of the cell?",
                "Nucleus", "Ribosome", "Mitochondria", "Chloroplast");
            break;
            case 4:
            loadQuestion("What is the unit of electric current?"
               , "Volt", "Watt","Ampere","Ohm");
            break;
            case 5:
            loadQuestion("Which part of the human brain controls "
                + "balance and coordination? ", "Cerebrum", "Cerebellum"
                ,"Medulla", "Hypothalamus");
            break;
            case 6:
            feedbackLabel.setVisible(false);
            submitButton.setVisible(false);
            nextButton.setText("Next Quiz");
            nextButton.setEnabled(true);
            answer_one.setVisible(false);
            answer_two.setVisible(false);
            answer_three.setVisible(false);
            answer_four.setVisible(false);
            timerLabel.setVisible(false);
            questionLabel.setText("Move to the next quiz (Computing) by clicking on Next button");
            QuizUtilities.timer.stop();
            innerBackground.setLayout(null);
            scoreLabel.setFont(new java.awt.Font("Comic Sans MS", java.awt.Font.BOLD, 40));
            scoreLabel.setText("Final Score: " + score + " / 5");
            scoreLabel.setBounds(130, 100, 500, 100);
            progressBar.setBounds(150, 200, 300, 60);
            questionLabel.setBounds(60, 270, 590, 60);
            nextButton.setBounds(210, 335, 176, 64);
            currentQuestion++;
            break;
            default:
            new EasyComputerQuiz().setVisible(true);
            this.dispose();
            break;
        }
        int progress = (currentQuestion * 100) / 6;
        progressBar.setValue(progress);
        progressBar.setStringPainted(true);
    }//GEN-LAST:event_nextButtonActionPerformed

