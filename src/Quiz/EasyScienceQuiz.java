    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        boolean isCorrect = false;
        switch (currentQuestion) {
            case 1:
            if (answer_one.isSelected()) {
                isCorrect = true;
            }
            break;
            case 2:
            if (answer_two.isSelected()) {
                isCorrect = true;
            }
            break;
            case 3:
            if (answer_three.isSelected()) {
                isCorrect = true;
            }
            break;
            case 4:
            if (answer_three.isSelected()) {
                isCorrect = true;
            }
            break;
            case 5:
            if (answer_two.isSelected()) {
                isCorrect = true;
            }
            break;
            default:
            JOptionPane.showMessageDialog(this, "Invalid question number.");
            return;
        }

        if(answer_one.isSelected() || answer_two.isSelected() || answer_three.isSelected() || answer_four.isSelected()) {
            currentQuestion++;
            submitButton.setEnabled(false);
            nextButton.setEnabled(true);
            if(isCorrect) {
                score++;
                QuizUtilities.scoreHistory.add(1);
                scoreLabel.setText("Score: " + score + " / 5");

                feedbackLabel.setText("Your answer is correct. Click on the next button to proceed.");

            }
            else{
                feedbackLabel.setText("Your answer is not correct. Click on the next button to proceed.");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Please select one option from the answers given.");
        }
    }//GEN-LAST:event_submitButtonActionPerformed

